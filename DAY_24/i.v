bvrbw5.5 Varactor Diode
The diode whose internal capacitance varies with the variation of the reverse voltage
such type of diode is known as the Varactor diode. It is used for storing the charge.
The varactor diode always works in reverse bias, and it is a voltage-dependent
semiconductor device.
The voltage-dependent device means the output of the diode depends on their
input voltage. The varactor diode is used in a place where the variable capacitance is
required, and that capacitance is controlled with the help of the voltage. The
Varactor diode is also known as the Varicap, Voltcap, Voltage variable
capacitance or Tuning diode.

The symbol of the varactor diode is similar to that of the PN-junction diode. The diode has
two terminals namely anode and cathode. The one end of a symbol consists the diode, and
their other end has two parallel lines that represent the conductive plates of the capacitor.
The gap between the plates shows their dielectric.

Construction of Varactor Diode
It is formed of P - type and N-type semiconductor and reverse biasing is applied to it.
The majority carriers in an N-type semiconductor are electrons and the majority carriers
in a P-type semiconductor are holes. At the junction, the electrons and holes
recombine. Due to which immobile ions accumulate at the junction. And no more current
can flow due to majority carriers Thus, the depletion region is formed. The depletion
region is called so because it is depleted of charge carriers i.e. the majority carriers are
absent in depletion region. This works as a dielectric layer and P and N-type
semiconductor works as plates of a capacitor.
Working of Varactor Diode

The Varactor diode is made up of n-type and p-type semiconductor material. In an n-
type semiconductor material, the electrons are the majority charge carrier and in the p-
type material, the holes are the majority carriers. When the p-type and n-type

semiconductor material are joined together, the p-n junction is formed, and the depletion
region is created at the PN-junction. The positive and negative ions make the depletion
region. This region blocks the current to enter from the PN-region.
Varactor diode operates only in reverse bias, Because of reverse bias, the current does
not flow. If the diodes is connected in forward bias the current starts flowing through the
diode and their depletion region become decreases. The depletion region does not allow
the ions to move from one place to another.

